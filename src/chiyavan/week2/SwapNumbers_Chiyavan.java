package chiyavan.week2;

public class SwapNumbers_Chiyavan {

    public static void main(String[] args) {

        int num1 = 9;
        int num2 = -18;

        System.out.println("Before: num1 = " + num1 + " num2 = " + num2);

        num1 += num2;
        num2 = num1 - num2;
        num1 = num1 - num2;

        System.out.println("Before: num1 = " + num1 + " num2 = " + num2);

    }


}

