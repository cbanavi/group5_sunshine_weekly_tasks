package faranak.week2;

public class SwapNumbers {

    public static void main(String[] args) {

        int x = 12;
        int y = 18;
        System.out.println("Before swap: x = " + x + ", y = " + y);

        int temp = x;
        x = y;
        y = temp;

        System.out.println("After swap: x = " + x + ", y = " + y);

    }
}


